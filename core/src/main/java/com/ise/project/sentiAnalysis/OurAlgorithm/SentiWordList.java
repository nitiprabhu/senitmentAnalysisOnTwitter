     public static Integer generateValue(List<String> finalList)
      {
        Integer result =0;
        for (int i=0; i< finalList.size();i++)
        {
            if (NegationClause.isNegativeClause(finalList.get(i)) && finalList.size()!=i+1)
            {
                String a,b;
                a=finalList.get(i); i++;
                b=finalList.get(i); //i++;
                Integer score = (-1) * getValue(b);
                result=score + result;
            }
            else
            {
                result= result + getValue(finalList.get(i));
            }
        }

          logger.info("Result of Tweet {}",result);
          return result;
     }



        public static boolean isSentiWord(String sentiWord)
        {
            generateSentiWord();
            if (sentiWord.contains(sentiWord))
                return true;
            return false;
        }

    public static Integer getValue(String s)

    {
        generateSentiWord();

       if(sentiWordMap.containsKey(s)) {

           logger.info("{} = {}",s,sentiWordMap.get(s));
           return sentiWordMap.get(s);
       }

        String checkAgain = getAntonyms(s);
        if (sentiWordMap.containsKey(checkAgain))
        {
            logger.info("Antonyms({}) =  {}", NegationWords.antonyms.get(checkAgain),(-1*sentiWordMap.get(checkAgain)) );
//            System.out.println("---->>" + (-1*sentiWordMap.get(checkAgain)));
            return (-1*sentiWordMap.get(checkAgain));
        }
        else
        {
            logger.warn("{} = NotFound",s);
            return 0;
        }
    }

